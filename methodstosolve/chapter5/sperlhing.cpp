void solve() {
  string s1, s2;
  cin >> s1 >> s2;
  int n = s1.length(), m = s2.length();

  int minimum = min(n, m);
  int count = 0;
  for (int i = 0; i < minimum; ++i) {
    if (s1[i] == s2[i])
      count++;
    else
      break;
  }

  cout << n + m - 2 * count << "\n";
}

